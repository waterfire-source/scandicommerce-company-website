const getPartnerLogo = (logoIcon?: string) => {
  switch (logoIcon) {
    case 'shopify':
      return <ShopifyIcon />
    case 'klaviyo':
      return <KlaviyoIcon />
    case 'google':
      return <GoogleIcon />
    case 'meta':
      return <MetaIcon />
    default:
      return <div className="w-12 h-12 bg-gray-300 rounded" />
  }
}

export default function TrustedPartnerships({ trustedPartnerships }: TrustedPartnershipsProps) {
  const title = trustedPartnerships?.title || 'Trusted partnerships'
  const subtitle = trustedPartnerships?.subtitle || 'Official certifications and partnerships that prove our expertise.'
  const partnerships = trustedPartnerships?.partnerships && trustedPartnerships.partnerships.length > 0 ? trustedPartnerships.partnerships : defaultPartnerships

  return (
    <section className="bg-[#F8F8F8] py-16 lg:py-24">
      <div className="container mx-auto px-4 sm:px-6 lg:px-8">
        {/* Title and Subtitle */}
        <div className="text-center mb-12 lg:mb-16 flex flex-col justify-start items-center gap-16">
          <h2 className="text-2xl sm:text-4xl lg:text-5xl font-bold text-[#222222]">
            {title}
          </h2>
          <p className="text-sm sm:text-lg lg:text-xl text-[#555555] max-w-2xl mx-auto">
            {subtitle}
          </p>
        </div>

        {/* Partnership Cards */}
        <div className="grid grid-cols-1 sm:grid-cols-2 lg:grid-cols-4 gap-6 lg:gap-8">
          {partnerships.map((partnership, index) => (
            <div
              key={index}
              className="bg-white shadow-md p-6 lg:p-8 flex flex-col items-center text-center"
            >
              {/* Logo */}
              <div className="mb-4">{getPartnerLogo(partnership.logoIcon)}</div>

              {/* Company Name */}
              <h3 className="text-sm md:text-base lg:text-base xl:text-lg 2xl:text-xl font-bold text-[#565454] mb-2">
                {partnership.name}
              </h3>

              {/* Partnership Status */}
              <p className="text-xs xl:text-sm 2xl:text-base text-[#565454]">
                {partnership.status}
              </p>
            </div>
          ))}
        </div>
      </div>
    </section>
  )
}
